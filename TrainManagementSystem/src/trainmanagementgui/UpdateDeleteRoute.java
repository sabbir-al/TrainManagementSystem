        try {
            // make the result set scrolable forward/backward updatable
//            statement = con.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_UPDATABLE);

            if (isValidData()) {
              //Promts the user to confirm choice              
            int confirm_update = JOptionPane.showConfirmDialog(this, "Confirm To Update Customer Trainline?");
            if (confirm_update == 0) {
                
               rs = dbCon.executeStatementQuery("Select * from ROUTES Where Origin = " +"(Select LocationID FROM LOCATION Where StationName = '"+cmbOrigin.getSelectedItem().toString() + "')"+" AND Destination = "+ "(Select LocationID FROM LOCATION Where StationName = '"+cmbDestination.getSelectedItem().toString() + "')");
               if(!rs.next()){
                    String prepareSQL = "UPDATE ROUTES SET  Origin = "
                    + "(Select LocationID FROM LOCATION Where StationName = '"+cmbOrigin.getSelectedItem().toString() + "')"
                    + "(Select LocationID FROM LOCATION Where StationName = '"+cmbDestination.getSelectedItem().toString() + "')"
                    + " WHERE RouteID = " + Integer.parseInt(txtRouteID.getText());   

                    int result = dbCon.executePreparedStmnt(prepareSQL);


                    if (result > 0) {

                        javax.swing.JLabel label = new javax.swing.JLabel("Route ID " + Integer.parseInt(txtRouteID.getText()) + " updated successfully.");
                        label.setFont(new java.awt.Font("Arial", java.awt.Font.BOLD, 18));
                        JOptionPane.showMessageDialog(null, label, "SUCCESS", JOptionPane.INFORMATION_MESSAGE);

                        getNewData();

                    }
                   
                   
               }
               
               else{
                   JOptionPane.showMessageDialog(this, "Same Route Already Exists!!");
               }
               
               
               
               

            }  
            
            } else {

                javax.swing.JLabel label = new javax.swing.JLabel("Please fix validation errors...");
                label.setFont(new java.awt.Font("Arial", java.awt.Font.BOLD, 18));
                JOptionPane.showMessageDialog(null, label, "ERROR", JOptionPane.ERROR_MESSAGE);

            }

        } catch (SQLException e) {

            JOptionPane.showMessageDialog(null, "Error updating Location." + e.getMessage());

        }
