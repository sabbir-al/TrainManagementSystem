    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:

        try {
            int confirm_delete = JOptionPane.showConfirmDialog(this, "Confirm to Delete Route?");
            if (confirm_delete == 0) {
            
             //Checks if route exists for any route allocation
            rs2 = dbCon.executeStatementQuery("SELECT * FROM Allocation where RouteID = " + Integer.parseInt(txtRouteID.getText().trim()));

            //Deletes the Location if not allocated to any route
            if(!rs2.next()){
                String prepSQL = "DELETE Routes WHERE RouteID = " + Integer.parseInt(txtRouteID.getText().trim());
                int result = dbCon.executePreparedStmnt(prepSQL);

                
                if (result > 0) {
                javax.swing.JLabel label = new javax.swing.JLabel("Route ID " + Integer.parseInt(txtRouteID.getText().trim()) + " deleted successfully.");
                label.setFont(new java.awt.Font("Arial", java.awt.Font.BOLD, 18));
                JOptionPane.showMessageDialog(null, label, "SUCCESS", JOptionPane.INFORMATION_MESSAGE);
                getNewData();
              }
                else {
                    javax.swing.JLabel label = new javax.swing.JLabel("Route Delete Failed.");
                    label.setFont(new java.awt.Font("Arial", java.awt.Font.BOLD, 18));
                    JOptionPane.showMessageDialog(null, label, "ERROR", JOptionPane.ERROR_MESSAGE);
                }
            }
           
            else{
                 JOptionPane.showMessageDialog(this, "Cannot Delete Route. Route is allocated to Trainlines!!");
            }

            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error deleting Route." + e);

        }

    }//GEN-LAST:event_btnDeleteActionPerformed

